public class SwitchStatement {
	
	public static void main(String[] args){
		switch("Dimas"){
			case "dimas" :
				System.out.println("Nama saya dimas");
				break;
			case "Dimas" :
				System.out.println("Nama saya Dimas");
				break;
			case "dimas1" :
				System.out.println("Nama saya Dimas");
				break;
			default :
				System.out.println("tidak ada yang sama");
		}
	}
}
